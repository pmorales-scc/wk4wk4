from math import *

# (4 - 4 + 4 - 4) is 0
print("In python, zero is: ", (4 - 4 + 4 - 4))

# (4 / 4) - (4 - 4) is 1
print("In python, one is: ", (4 / 4) - (4 - 4))

# (4 - 4) + (4 - sqrt(4)) is 2
print("In python, two is: ", (4 - 4) + (4 - sqrt(4)))

# (sqrt(4) + sqrt(4) - 4/4) is 3
print("In python, three is: ", (sqrt(4) + sqrt(4) - 4/4))

# 4 - 4 + 4 + sqrt(4) is 6
print("In python, six is: ", (4 - 4 + 4 + sqrt(4)))

# (sqrt(4) + sqrt(4) + sqrt(4) + sqrt(4)) is 8
print("In python, eight is: ", (sqrt(4) + sqrt(4) + sqrt(4) + sqrt(4)))

# (4! - sqrt(4) + sqrt(4) - 4)
print("In python, twenty is: ", (factorial(4) - sqrt(4) + sqrt(4) - 4))



